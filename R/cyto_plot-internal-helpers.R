#' Get gate centers to use to position labels
#'
#' @param x gate object.
#' @param channels channels used to construct the plot.
#' @param text_x x co-ordinate for label.
#' @param text_y y co-ordinate for label.
#'
#' @importFrom graphics par
#' @importFrom flowCore parameters Subset rectangleGate
#'
#' @noRd
.cyto_plot_label_center <- function(x, ...) {
  UseMethod(".cyto_plot_label_center")
}

#' @noRd
.cyto_plot_label_center.default <- function(x,
                                            channels,
                                            text_x = NA,
                                            text_y = NA) {

  # NULL or NA gate
  if (.all_na(x) | is.null(x)) {

    # No gates - use plot limits to set co-ordinates for label
    xmin <- par("usr")[1]
    xmax <- par("usr")[2]
    ymin <- par("usr")[3]
    ymax <- par("usr")[4]

    # x label position - defaults to 3/4 * range of x axis
    if (.all_na(text_x)) {
      text_x <- xmin + (xmax - xmin) * 0.75
    }

    # y label position - defaults to 1/2 * range of y axis
    if (.all_na(text_y)) {
      text_y <- (ymax - ymin) * 0.5
    }

    # Return co-ordinates
    text_xy <- matrix(c(text_x, text_y),
      dimnames = list(c("x", "y")),
      byrow = TRUE,
      nrow = 2
    )

    return(text_xy)
  } else {
    stop(paste0(
      "cyto_plot_label does not support gate objects of ",
      class(x), "!"
    ))
  }
}

#' @noRd
.cyto_plot_label_center.rectangleGate <- function(x,
                                                  channels,
                                                  text_x = NA,
                                                  text_y = NA) {

  # 1D gate plotted in 2D
  if (length(channels) == 2 & length(parameters(x)) == 1) {
    rg <- matrix(c(as.numeric(x@min), as.numeric(x@max), -Inf, Inf),
      ncol = 2, nrow = 2
    )
    colnames(rg) <- c(
      as.vector(parameters(x)),
      channels[!channels == as.vector(parameters(x))]
    )
    rownames(rg) <- c("min", "max")
    x <- rectangleGate(.gate = rg)
  }

  # Gate channels
  chans <- as.vector(parameters(x))

  # Plot limits
  xmin <- par("usr")[1]
  xmax <- par("usr")[2]
  ymin <- par("usr")[3]
  ymax <- par("usr")[4]

  # 1D gate supplied
  if (length(chans) == 1) {
    if (length(channels) == 1) {
      if (!channels == chans) {
        stop("Supplied channel does not match that of the supplied gate.")
      } else if (channels == chans) {
        if (.all_na(text_x)) {
          xmin <- x@min
          xmax <- x@max

          if (is.infinite(xmin)) {
            xmin <- par("usr")[1]
          }
          if (is.infinite(xmax)) {
            xmax <- par("usr")[2]
          }
        }
      }
    } else if (length(channels) == 2) {
      if (!chans %in% channels) {
        stop("Supplied channels do not match that of the supplied gate.")
      } else if (chans %in% channels) {
        if (.all_na(text_x)) {
          xmin <- x@min[channels[1]]
          xmax <- x@max[channels[1]]

          if (is.infinite(xmin)) {
            xmin <- par("usr")[1]
          }

          if (is.infinite(xmax)) {
            xmax <- par("usr")[2]
          }
        }

        if (.all_na(text_y)) {
          ymin <- x@min[channels[2]]
          ymax <- x@max[channels[2]]

          if (is.infinite(ymin)) {
            ymin <- par("usr")[3]
          }

          if (is.infinite(ymax)) {
            ymax <- par("usr")[4]
          }
        }
      }
    }

    # 2D gate supplied
  } else if (length(chans) == 2) {
    if (!all(chans %in% c("y", channels))) {
      stop("Supplied channels do not match that of the supplied gate.")
    }

    if (.all_na(text_x)) {
      xmin <- x@min[channels[1]]
      xmax <- x@max[channels[1]]

      if (is.infinite(xmin)) {
        xmin <- par("usr")[1]
      }

      if (is.infinite(xmax)) {
        xmax <- par("usr")[2]
      }
    }

    if (.all_na(text_y)) {
      if ("y" %in% chans) {
        ychan <- "y"
      } else {
        ychan <- channels[2]
      }
      ymin <- x@min[ychan]
      ymax <- x@max[ychan]

      if (is.infinite(ymin)) {
        ymin <- par("usr")[3]
      }

      if (is.infinite(ymax)) {
        ymax <- par("usr")[4]
      }
    }
  }

  # Label position - x - gate center
  if (.all_na(text_x)) {
    text_x <- c(xmin + xmax) / 2
  }

  # Label position - y - gate center
  if (.all_na(text_y)) {
    text_y <- c(ymin + ymax) / 2
  }

  # Return co-ordinates
  text_xy <- matrix(c(text_x, text_y),
    dimnames = list(c("x", "y")),
    byrow = TRUE,
    nrow = 2
  )

  return(text_xy)
}

#' @noRd
.cyto_plot_label_center.polygonGate <- function(x,
                                                channels,
                                                text_x = NA,
                                                text_y = NA) {

  # Check supplied channels & gate channels
  chans <- parameters(x)

  # Check gate parameters against channels
  if (!all(chans %in% channels)) {
    stop("Supplied channels do not match that of the supplied gate.")
  }

  # Label position - x
  if (.all_na(text_x)) {
    text_x <- sum(x@boundaries[, channels[1]]) / nrow(x@boundaries)
  }

  # Label position - y
  if (.all_na(text_y)) {
    text_y <- sum(x@boundaries[, channels[2]]) / nrow(x@boundaries)
  }

  # Return co-ordinates
  text_xy <- matrix(c(text_x, text_y),
    dimnames = list(c("x", "y")),
    byrow = TRUE,
    nrow = 2
  )

  return(text_xy)
}

